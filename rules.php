        <p align="justify">Evaluation system for degree engineering (B.E.) is divided in to two part viz. Theory and Practical component. The same pattern is also followed for Master in Engineering (M.E.). The theory (written exam) component is further divided into two sub components. 70% weightage is given to External Exam conducted by the university and 30% weightage is for Mid Semester Exam conducted by the institute. The practical component of the examination consisting of 50 marks, is divided into two sub components: Internal component (20 marks) and Viva Voce (30 marks) The marks for the internal component are to be awarded by the instructor on the basis of assignments, seminars, quizzes, attendance and practical work etc. The scheme of assessment is to be decided by the instructor of the course and announced at the beginning of the course. For the component of Viva Voce, the pattern is different for semester 1 to 4 and semester 5 to 8 of B.E. The pattern for M.E, is same as followed for BE semester 5 to 8. For BE semester 1 to 4, the Viva Voce has to be conducted by the instructor of the course at the end of the semester while taking the submission of the term work/assignments. For BE semester 5 to 8 and M.E, the Viva Voce is to be conducted by the external faculty appointed by the university. The final grade of the subject is given after adding the marks secured by the student in all the components. The Marks will be converted to grade and then to grade point. Semester progressive index is then calculated using the grade points and credit of the subject.
